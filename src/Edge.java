

public class Edge {
    private int weight;
    private String vt;

    public Edge(int weight, String vt) {
        this.weight = weight;
        this.vt = vt;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public String getVt() {
        return vt;
    }

    public void setVt(String vt) {
        this.vt = vt;
    }
}
